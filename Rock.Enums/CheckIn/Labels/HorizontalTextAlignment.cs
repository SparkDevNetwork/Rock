namespace Rock.Enums.CheckIn.Labels
{
    /// <summary>
    /// The possible horizontal text alignment values for a text field.
    /// </summary>
    public enum HorizontalTextAlignment
    {
        /// <summary>
        /// The text is aligned to the left edge of the field area.
        /// </summary>
        Left = 0,

        /// <summary>
        /// The text is aligned in the center of the field area.
        /// </summary>
        Center = 1,

        /// <summary>
        /// The text is aligned to the right edge of the field area.
        /// </summary>
        Right = 2
    }
}
