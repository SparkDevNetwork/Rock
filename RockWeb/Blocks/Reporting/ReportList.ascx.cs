using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.IO;
using System.Linq;
using System.Web.UI;
using System.Web.UI.WebControls;

using Rock;
using Rock.Data;
using Rock.Model;
using Rock.Web.Cache;
using Rock.Web.UI.Controls;
using Rock.Attribute;
using Rock.Web.UI;

namespace RockWeb.Blocks.Reporting
{
    [DisplayName( "Report List" )]
    [Category( "Reporting" )]
    [Description( "Lists all reports under a specified report category." )]
    [CategoryField( "Report Category", "Category to use to list reports for.", false, "Rock.Model.Report", "", "", true, "89e54497-5e98-4f1b-b83a-95bfb685da91", "" )]
    [LinkedPage("Detail Page", "Detail page to show report.", true, "", "", 1)]
    [Rock.SystemGuid.BlockTypeGuid( "37D29989-F7CA-4D51-925A-378DB73ED53D" )]
    public partial class ReportList : RockBlock, ICustomGridColumns
    {
        #region Fields

        // used for private variables

        #endregion

        #region Properties

        // used for public / protected properties

        #endregion

        #region Base Control Methods

        //  overrides of the base RockBlock methods (i.e. OnInit, OnLoad)

        /// <summary>
        /// Raises the <see cref="E:System.Web.UI.Control.Init" /> event.
        /// </summary>
        /// <param name="e">An <see cref="T:System.EventArgs" /> object that contains the event data.</param>
        protected override void OnInit( EventArgs e )
        {
            base.OnInit( e );
            gList.DataKeyNames = new[] { "id" };
            gList.GridRebind += gList_GridRebind;

            // this event gets fired after block settings are updated. it's nice to repaint the screen if these settings would alter it
            this.BlockUpdated += Block_BlockUpdated;
            this.AddConfigurationUpdateTrigger( upnlContent );
        }

        /// <summary>
        /// Raises the <see cref="E:System.Web.UI.Control.Load" /> event.
        /// </summary>
        /// <param name="e">The <see cref="T:System.EventArgs" /> object that contains the event data.</param>
        protected override void OnLoad( EventArgs e )
        {
            if ( !Page.IsPostBack )
            {
                BindGrid();
            }

            base.OnLoad( e );
        }

        #endregion

        #region Events

        // handlers called by the controls on your block

        /// <summary>
        /// Handles the BlockUpdated event of the control.
        /// </summary>
        /// <param name="sender">The source of the event.</param>
        /// <param name="e">The <see cref="EventArgs"/> instance containing the event data.</param>
        protected void Block_BlockUpdated( object sender, EventArgs e )
        {
            BindGrid();
        }

        protected void gList_RowSelected( object sender, RowEventArgs e )
        {
            int selectedId = e.RowKeyId;

            NavigateToLinkedPage( "DetailPage", "ReportId", selectedId );
        }

        /// <summary>
        /// Handles the GridRebind event of the gList control.
        /// </summary>
        /// <param name="sender">The source of the event.</param>
        /// <param name="e">The <see cref="EventArgs"/> instance containing the event data.</param>
        private void gList_GridRebind( object sender, EventArgs e )
        {
            BindGrid();
        }

        #endregion

        #region Methods

        /// <summary>
        /// Binds the grid.
        /// </summary>
        private void BindGrid()
        {
            RockContext rockContext = new RockContext();
            ReportService reportService = new ReportService( rockContext );

            Guid reportCategory = new Guid(GetAttributeValue( "ReportCategory" ));

            // sample query to display a few people
            var qry = reportService.Queryable()
                        .Where( r => r.Category.Guid == reportCategory );

            gList.DataSource = qry.ToList();
            gList.DataBind();
        }

        #endregion
    }
}